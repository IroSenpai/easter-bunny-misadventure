/**
 * Интерфейс HideStrategy, който дефинира методи за скриване на яйце и изпълнение на допълнителни действия.
 */
public interface HideStrategy {

    /**
     * Метод за скриване на яйце.
     */
    void hide();

    /**
     * Метод за изпълнение на допълнителни действия преди скриването на яйцето.
     */
    void performsAction();
}
